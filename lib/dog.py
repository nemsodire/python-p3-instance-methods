#!/usr/bin/env python3

class Dog:
    # Class body goes here

    # Instance method definition
    def bark(self):
        print("Woof!")
    
    def sit(self):
        print("The dog is sitting.")

# Creating instances of Dog and calling their methods
fido = Dog()
fido.bark()
fido.sit()

snoopy = Dog()
snoopy.bark()
snoopy.sit()

